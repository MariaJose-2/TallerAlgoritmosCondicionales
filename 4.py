"""""
Diseña un algoritmo que lea 2 números y visualice si son positivos.
"""
number1 = float(input("enter the first number: "))
number2 = float(input("enter the second number: "))
if number1 > 0:
    print(f"the first number is positive.")
else:
    print(f"the first number is negative")
    
if number2 > 0:
    print(f"The second number is positive.")
else:
    print(f"the second number is negative")
